package csacademy.round014;

import java.io.IOException;
import java.io.InputStream;
import java.io.PrintWriter;
import java.util.Arrays;
import java.util.InputMismatchException;

public class E {
    public static void main(String[] args) {
        InputReader in = new InputReader(System.in);
        PrintWriter out = new PrintWriter(System.out);

        int n = in.nextInt();
        D = in.nextInt();
        MOD = in.nextInt();

        dp = new int[320][320];
        dpSum = new int[320][320];

        dp[1][0] = 1;
        for (int v = 1 ; v <= n ; v++) {
            for (int h = 1 ; h <= D ; h++) {
                long sum = dp[v-1][h-1];

                int leftDepth = h-1;
                int otherFrom = 1;
                int otherTo = Math.min(h, D-leftDepth-1);

                int leftFrom = 0;
                int leftTo = Math.min(h-2, D-h-1);
                int otherDepth = h;

                for (int c = 1 ; c <= v-1 ; c++) {
                    long right = (otherFrom > otherTo) ? 0 : (dpSum[v-c][otherTo+1]-dpSum[v-c][otherFrom]+MOD)%MOD;
                    sum += right * dp[c][leftDepth] % MOD;

                    long left = (leftFrom > leftTo) ? 0 : (dpSum[c][leftTo+1]-dpSum[c][leftFrom]+MOD)%MOD;
                    sum += left * dp[v-c][otherDepth] % MOD;
                }
                dp[v][h] = (int)(sum % MOD);
            }

            for (int i = 1 ; i < dpSum[0].length ; i++) {
                dpSum[v][i] += dpSum[v][i-1] + dp[v][i-1];
                dpSum[v][i] %= MOD;
            }
        }

        long sum = 0;
        for (int i = 1 ; i <= D; i++) {
            sum += dp[n][i];
        }
        out.println(sum % MOD);
        out.flush();
    }

    private static int MOD;

    private static int D;

    private static int[][] dp;

    private static int[][] dpSum = new int[320][320];

    static class InputReader {
        private InputStream stream;
        private byte[] buf = new byte[1024];
        private int curChar;
        private int numChars;

        public InputReader(InputStream stream) {
            this.stream = stream;
        }

        private int[] nextInts(int n) {
            int[] ret = new int[n];
            for (int i = 0; i < n; i++) {
                ret[i] = nextInt();
            }
            return ret;
        }

        private int[][] nextIntTable(int n, int m) {
            int[][] ret = new int[n][m];
            for (int i = 0; i < n; i++) {
                for (int j = 0; j < m; j++) {
                    ret[i][j] = nextInt();
                }
            }
            return ret;
        }

        private long[] nextLongs(int n) {
            long[] ret = new long[n];
            for (int i = 0; i < n; i++) {
                ret[i] = nextLong();
            }
            return ret;
        }

        private long[][] nextLongTable(int n, int m) {
            long[][] ret = new long[n][m];
            for (int i = 0; i < n; i++) {
                for (int j = 0; j < m; j++) {
                    ret[i][j] = nextLong();
                }
            }
            return ret;
        }

        private double[] nextDoubles(int n) {
            double[] ret = new double[n];
            for (int i = 0; i < n; i++) {
                ret[i] = nextDouble();
            }
            return ret;
        }

        private int next() {
            if (numChars == -1)
                throw new InputMismatchException();
            if (curChar >= numChars) {
                curChar = 0;
                try {
                    numChars = stream.read(buf);
                } catch (IOException e) {
                    throw new InputMismatchException();
                }
                if (numChars <= 0)
                    return -1;
            }
            return buf[curChar++];
        }

        public char nextChar() {
            int c = next();
            while (isSpaceChar(c))
                c = next();
            if ('a' <= c && c <= 'z') {
                return (char) c;
            }
            if ('A' <= c && c <= 'Z') {
                return (char) c;
            }
            throw new InputMismatchException();
        }

        public String nextToken() {
            int c = next();
            while (isSpaceChar(c))
                c = next();
            StringBuilder res = new StringBuilder();
            do {
                res.append((char) c);
                c = next();
            } while (!isSpaceChar(c));
            return res.toString();
        }

        public int nextInt() {
            int c = next();
            while (isSpaceChar(c))
                c = next();
            int sgn = 1;
            if (c == '-') {
                sgn = -1;
                c = next();
            }
            int res = 0;
            do {
                if (c < '0' || c > '9')
                    throw new InputMismatchException();
                res *= 10;
                res += c-'0';
                c = next();
            } while (!isSpaceChar(c));
            return res*sgn;
        }

        public long nextLong() {
            int c = next();
            while (isSpaceChar(c))
                c = next();
            long sgn = 1;
            if (c == '-') {
                sgn = -1;
                c = next();
            }
            long res = 0;
            do {
                if (c < '0' || c > '9')
                    throw new InputMismatchException();
                res *= 10;
                res += c-'0';
                c = next();
            } while (!isSpaceChar(c));
            return res*sgn;
        }

        public double nextDouble() {
            return Double.valueOf(nextToken());
        }

        public boolean isSpaceChar(int c) {
            return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
        }

        public interface SpaceCharFilter {
            public boolean isSpaceChar(int ch);
        }
    }

    static void debug(Object... o) {
        System.err.println(Arrays.deepToString(o));
    }
}
